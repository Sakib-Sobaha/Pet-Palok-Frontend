function Login() {
  const [activeTab, setActiveTab] = useState("tab1");

  const userLogin = async (username, password) => {
    try {
      const response = await fetch("http://localhost:8080/api/v1/user/login", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({ username, password }),
      });

      if (!response.ok) {
        throw new Error("Login failed");
      }

      const data = await response.json();
      const token = data.token;

      // Store token in local storage
      localStorage.setItem("authToken", token);
      localStorage.userType("userType", "user");
    } catch (error) {
      console.error("Login error:", error);
    }
  };

  const handleTabClick = (tab) => {
    setActiveTab(tab);
  };

  return (
    <div>
      {/* 'API URL:'
      { process.env.REACT_APP_API_URL
      } */}
      <div role="tablist" className="tabs tabs-boxed">
        <a
          role="tab"
          className={`tab ${
            activeTab === "tab1" ? "tab-active" : ""
          } hover:bg-base-300`}
          onClick={() => handleTabClick("tab1")}
        >
          User
        </a>
        <a
          role="tab"
          className={`tab ${
            activeTab === "tab2" ? "tab-active" : ""
          } hover:bg-base-300`}
          onClick={() => handleTabClick("tab2")}
        >
          Vet
        </a>
        <a
          role="tab"
          className={`tab ${
            activeTab === "tab3" ? "tab-active" : ""
          } hover:bg-base-300`}
          onClick={() => handleTabClick("tab3")}
        >
          Seller
        </a>
        <a
          role="tab"
          className={`tab ${
            activeTab === "tab4" ? "tab-active" : ""
          } hover:bg-base-300`}
          onClick={() => handleTabClick("tab4")}
        >
          Admin
        </a>
      </div>

      {activeTab === "tab1" && (
        <LoginHero
          type="user"
          title="Welcome User!"
          text="This is the login for users."
          icon={UserIcon}
          loginURL={`${process.env.REACT_APP_API_URL}/auth/authenticate`}
          signupURL={`${process.env.REACT_APP_API_URL}/user/register`}
        />
      )}
      {activeTab === "tab2" && (
        <LoginHero
          type="vet"
          title="Welcome Abord, Vet!"
          text="This is the login for vets."
          icon={DoctorIcon}
          loginURL={`${process.env.REACT_APP_API_URL}/vet/authenticate`}
          signupURL={`${process.env.REACT_APP_API_URL}/vet/register`}

        />
      )}
      {activeTab === "tab3" && (
        <LoginHero
          type="seller"
          title="Hello Seller!"
          text="This is the login for sellers."
          icon={ShoppingCartIcon}
          loginURL={`${process.env.REACT_APP_API_URL}/seller/authenticate`}
          signupURL={`${process.env.REACT_APP_API_URL}/seller/register`}

        />
      )}
      {activeTab === "tab4" && (
        <LoginHero
          type="admin"
          title="Hey there, Admin!"
          text="This is the login for sellers."
          icon={adminIcon}
          loginURL={`${process.env.REACT_APP_API_URL}/admin/authenticate`}
          signupURL={`${process.env.REACT_APP_API_URL}/admin/register`}

        />
      )}
      {/* {url} */}
    </div>
  );
}

